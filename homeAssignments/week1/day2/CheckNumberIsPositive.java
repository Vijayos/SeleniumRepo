package homeAssignments.week1.day2;

public class CheckNumberIsPositive {

	public static void main(String[] args) {

		int variable = 10;

		if (variable > 0) {
			System.out.println(variable + " is Postive Number");
		} else if(variable < 0) {
			System.out.println(variable + " is Negative Number");
		} else {
			System.out.println(variable + " is Zero");
		}

	}
}
